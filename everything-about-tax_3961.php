                            <h1 class="post-title">Everything about Tax in the United States</h1>
                        </header>
                        <section class="post-content">
                            <p>Tax is a very complex issue and there is no one size fits all approach. Here is an example of various filings required for an imaginary Romanian citizen who runs a single person LLC in the United State remotely and sell SAAS application to his customers and also does e-commerce via Amazon FBA. This is an imaginary situation, your exact case may vary, for example depending on any tax treaty of United State with your country.</p>
                            <figure class="kg-card kg-image-card">
                                <img src="./photos/tax-example_910e_82.jpg" class="kg-image" alt>
                            </figure>
                            <h3 id="structure-background">Structure Background </h3>
                            <p>CodeSoft US, LLC is a United States Limited Liability Company which sells a variety of software and physical goods to U.S. and foreign customers. Gabriel is citizen and resident of Romania, and the sole owner of CodeSoft US, LLC. He maintains his offices in Romania, and is not physically present in the United Staes. As a Single-Member LLC (SMLLC), CodeSoft US, LLC is classified as a disregarded entity for Federal tax purposes per Treasury Regulation § 1 301.7701-3 2</p>
                            <p>The following is a summary of the products sold:</p>
                            <ol>
                                <li>Software as a Service (SaaS) monthly web application subscriptions and onetime purchase applications. The majority of the software sold is to U.S. customers using the software un the United States, and the remaining portion to foreign customers.</li>
                                <li>E-Commerce; Physical Widgets sold and shipped through Amazon FBA warehouse located in the United States and directly to U.S. customers. Source and Taxation of Royalty Income (SaaS Applications) For the use of copyrighted software (either used as a web application subscription, or one time purchase), royalties are sourced where the property was used per Internal Revenue Code § 861 . 3 4 Based on the Tax Treaty rate of Royalties earned in the United States, residents of Romania are taxed at a flat rate of 10% .</li>
                            </ol>
                            <p>Source and Taxation of Physical Goods (E-Commerce) All sales in the United States are sold and fulfilled through Amazon FBA. Once manufactured, the widgets are shipped directly from the supplier to an Amazon FBA warehouse in the United States. Once sold to a customer, the goods are then shipped from Amazon’s warehouse directly to customers throughout the United States. Amazon then pays CodeSoftUS, LLC for the revenue related to the purchase of goods (less any fulfilment/shipping fees).</p>
                            <p>Although the sale of goods from Amazon FBA warehouse to the U.S. customer is considered Effectively Connected Income (ECI) earned by the LLC per IRC 864(c)(3) , as the result of sale of inventory attributable to a U.S. shipping and 6 destination location per IRC, §865(e)(2) and 882 , the business profits of the 7 8 Limited Liability Company (and that of its Sole Non-Resident Owner) qualifies for a Tax Treaty Exclusion under the Romanian-U.S. Income Tax Treaty Articles 5 and 7 . 9.</p>
                            <p>Article 5 of the Romanian-U.S. Tax Treaty describes the definition of a Permanent Establishment. Article 7 of the Tax Treaty states “Industrial or commercial profits of a resident of one of the Contracting States shall be exempt from tax by the other Contracting State unless the resident has a permanent establishment in that other Contracting State.”</p>
                            <h3 id="monetary-transactions">Monetary Transactions </h3>
                            <ol>
                                <li>CodeSoftUS, LLC sells SaaS Subscriptions totalling $300,000, of which $210,000 is by U.S. customers using the software in the United Staes, and $90,000 of foreign customers using the software outside the United States.</li>
                                <li>CodeSoftUS, LLC pays a third party supplier company to manufacture widgets for $100,000 which are then shipped to the Amazon FBA warehouse. The widgets are then sold to U.S. customers for $300,000, resulting in a net profit from this activity of $200,000.</li>
                                <li>On its Income Statement, CodeSoftUS, LLC reports Net Income of $500,000 ($210,000 U.S. SaaS Subscriptions/Applications + $90,000 U.S. SaaS Subscriptions/Applications + $300,000 Product Sales through Amazon FBA E-Commerce - Less $100,000 Cost of Goods Sold).</li>
                            </ol>
                            <h3 id="tax-compliance">Tax Compliance </h3>
                            <p><strong>Form 1040NR (Gabriel)</strong>. As a sole owner of the disregarded LLC, Gabriel files Form 1040NR, U.S. Nonresident Alien Income Tax Return, to report the royalty income of $210,000 taxed at 10% rate, or a $21,000 withholding tax. Additionally, a Tax Treaty exclusion is claimed on Form 8833, Treaty-Based Return Position Disclosure Under Section 6114 or 7701(b) for the E-Commerce widget sales. The tax treaty article which directly applies is Article 7, Business Profits.</p>
                            <h3 id="result">Result </h3>
                            <p>As a result of this structure, the U.S. tax lability of Gabriel (as a Disregarded Entity Single Member LLC owner of CodeSoftUS, LLC) is $21,000.</p>
